<h2>CODE OF PROFESSIONAL CONDUCT</h2>
<p>Registered professionals and ICTA accreditation/certification holders shall:</p>
<ol>
<li>Perform their duties with objectivity, due diligence and care, in accordance with professional IT standards and procedures for effective governance and management of Information and Communications Technologies.</li>
<li>Serve for public good in a lawful manner, while maintaining high standards of conduct and character.</li>
<li>Maintain the privacy and confidentiality of information obtained in the course of their activities.</li>
<li>Perform services only in areas of their competence</li>
<li>Inform appropriate parties of the results of work performed including the full disclosure of all significant facts</li>
<li>Support the professional education of stakeholders in enhancing their understanding of the governance and effective management of information and communications technology.</li>
<li>Failure to comply with this Code of Professional Ethics can result in an investigation into a registered professional or accredited holderâ€™s conduct and, ultimately, in disciplinary measures including exclusion from the roll of IT professionals.</li>
</ol>